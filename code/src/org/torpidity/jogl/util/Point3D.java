package org.torpidity.jogl.util;

public class Point3D {
	private float x;
	private float y;
	private float z;
	
	public Point3D(float a, float b, float c) {
		this.x = a;
		this.y = b;
		this.z = c;
	}
	
	public float getX() {
		return x;
	}
	
	public float getY() {
		return y;
	}
	
	public float getZ() {
		return z;
	}
}
